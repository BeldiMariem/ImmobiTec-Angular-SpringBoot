    @Override
    public UserDTO getUserById(int id) {
        AppUser userApp =userRepository.findById(id).orElse(null);
        UserDTO userDTO=modelMapper.map(userApp, UserDTO.class);
        userDTO.setRole_id(userApp.getRole().getId());
        userDTO.setRole_name(userApp.getRole().getName());
        return userDTO;
    }
    @Override
    public AppUser addUser(UserDTO userDTO) {
        Rolee role=roleRepository.findById(userDTO.getRole_id()).orElse(null);
        AppUser user=modelMapper.map(userDTO, AppUser.class);
        user.setPassword(passwordEncoder.encode(userDTO.getPassword()));
        user.setRole(role);
        user.setEnabled(true);
        user.setWallet(1000000);
        adresseRepository.save(user.getAdresse());
        return userRepository.save(user);
    }

    @Override
    public boolean checkIfUserExist(String email) {
        return userRepository.findByEmail(email) !=null ? true : false;
    }
    @Override
    public AppUser update(UserDTO userDTO) {
        Rolee role = roleRepository.findById(userDTO.getRole_id()).orElse(null);
        AppUser user = userRepository.findById(userDTO.getId_user()).orElse(null);
        user = modelMapper.map(userDTO, AppUser.class);
        //user.setPassword(passwordEncoder.encode(userDTO.getPassword()));
        if (role != null)
            user.setRole(role);
        return userRepository.save(user);
    }

    public AppUser updatePass(UserDTO userDTO) {

        AppUser user = userRepository.findById(userDTO.getId_user()).orElse(null);
        user = modelMapper.map(userDTO, AppUser.class);
        user.setPassword(passwordEncoder.encode(userDTO.getPassword()));

        return userRepository.save(user);
    }
    @Override
    public AppUser register(UserDTO userDTO, String siteURL)  throws  UnsupportedEncodingException, MessagingException,Exception {
        Rolee role=roleRepository.findById(userDTO.getRole_id()).orElse(null);
        AppUser user=modelMapper.map(userDTO, AppUser.class);
        user.setPassword(passwordEncoder.encode(userDTO.getPassword()));
        user.setRole(role);
        user.setWallet(1000000);
        if(checkIfUserExist(user.getEmail())){
                 throw new UserExistException("user already exist");
        }
        else{
            String randomCode = RandomString.make(64);
            user.setVerificationCode(randomCode);
            user.setEnabled(false);

            userRepository.save(user);
            sendVerificationEmail(user, siteURL);
            return user;
        }
    }

